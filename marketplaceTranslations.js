// Helper function to get marketplace translation
export const getMarketplaceTranslation = (lang, key) => {
  const langCode = lang === 'ae' ? 'ar' : lang === 'xk' ? 'sq' : lang === 'gb' ? 'en' : lang;
  const translations = marketplaceTranslations[langCode] || marketplaceTranslations['en'];
  return translations?.marketplace?.[key] || marketplaceTranslations['en']?.marketplace?.[key] || key;
};
